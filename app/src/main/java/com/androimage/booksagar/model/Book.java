    public Book(int id, int seller_id, String title, String author, String publisher, int year, String mrp, String price, String standard, String language, boolean verified, boolean sold, boolean noshared, String added_on, String imageUrl, String description, String location, String type) {
        this.id = id;
        this.seller_id = seller_id;
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.year = year;
        this.mrp = mrp;
        this.price = price;
        this.sold = sold;
        this.standard = standard;
        this.language = language;
        this.verified = verified;
        this.noshared = noshared;
        this.imageUrl = imageUrl;
        this.description = description;
        this.type = type;
        this.location = location;


        try {
            SimpleDateFormat fromUser = new SimpleDateFormat("yyyy-MM-dd");


            SimpleDateFormat formatter = new SimpleDateFormat("MMM dd yyyy");
            this.added_on = formatter.format(fromUser.parse(added_on.substring(0, 9)));
        } catch (ParseException e) {
            e.printStackTrace();
        }

    }

